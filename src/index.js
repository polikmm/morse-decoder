const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let result = '',
        current = expr.split('');
    while (current.length) {
        let temp = current.splice(0, 10).join('')
        temp = temp.replace(/10/g, '.')
        temp = temp.replace(/11/g, '-')
        temp = temp.replace(/0/g, '')
        if (temp.match(/\*+/)) result = `${result} `
        if (!temp.match(/\*+/)) result = `${result}${MORSE_TABLE[temp]}`
    }
    return result
}

module.exports = {
    decode
}